package com.tank.model;

public class GroupNoteBook {

	String courseTitle;
	int myNoteNum;
	int otherNoteNum;
	
	public String getCourseTitle() {
		return courseTitle;
	}
	public void setCourseTitle(String courseTitle) {
		this.courseTitle = courseTitle;
	}
	public int getMyNoteNum() {
		return myNoteNum;
	}
	public void setMyNoteNum(int myNoteNum) {
		this.myNoteNum = myNoteNum;
	}
	public int getOtherNoteNum() {
		return otherNoteNum;
	}
	public void setOtherNoteNum(int otherNoteNum) {
		this.otherNoteNum = otherNoteNum;
	}
}
